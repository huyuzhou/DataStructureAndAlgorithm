//2、孩子表示法
typedef struct _enode {
	int childindex; //孩子结点的数组下标索引
	struct _enode* next;//下一个孩子结点指针
}enode, * p_enode;

typedef struct _vnode {
	int key;//关键字
	int value;
	p_enode childs;//结点的孩子结点链表
}vnode, * p_vnode;

typedef struct _childs_tree {
	p_vnode vnode_list_head;//存储数据组的头指针
	int vnum;//树中结点数
	int rootindex;//树根结点下标索引
}childs_tree, * p_childs_tree;