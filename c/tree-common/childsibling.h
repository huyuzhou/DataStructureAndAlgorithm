//3、孩子兄弟表示法（就是二叉树的表示法）
typedef struct _vnode {
	int key;//关键字
	int value;//数据
	struct _vnode* firstchild;//左孩子
	struct _vnode* sibling;//兄弟结点
}vnode, * p_vnode;

typedef struct _childs_tree {
	p_vnode root;//树根结点
	int vnum;//树中结点数
}childs_tree, * p_childs_tree;